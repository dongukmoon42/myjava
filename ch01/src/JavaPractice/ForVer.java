package JavaPractice;

public class ForVer {

	public static void main(String[] args) {
		// For제어문
		/*
		 for(선언; 조건; 증감){
		 	명령문1
		 	명령문2
		 	...
		 }
		 선언: 정수형 변수 i를 선언 및 0으로 초기화
		 조건: i가 5보다 작다는 조건을 만족하는동안 반복문 내 명령문 실행
		 증감: i의 값을 1증가
		 */

		for (int i = 0; i < 5; i++) {
			System.out.println("hello?");
		}
	}

}
